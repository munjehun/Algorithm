function solution(n, arr1, arr2) {
  //2ì§„ìˆ˜í™”
  arr1 = arr1.map((i) => i.toString(2));
  arr2 = arr2.map((i) => i.toString(2));

  let answer = arr1
    .map((i, index) => +i + +arr2[index])
    .map((i) => i + "")
    .map((i) =>
      i
        .padStart(n, "0")
        .split("")
        .map((i) => {
          return i == 0 ? " " : "#";
        })
        .join("")
    );
  return answer;
}

