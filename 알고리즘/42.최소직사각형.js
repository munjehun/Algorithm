function solution(sizes) {
  sizes = sizes.map((i) => i.sort((a, b) => a - b));

  let maxSize = [];
  for (let j = 0; j < 2; j++) {
    maxSize.push(
      sizes.reduce((a, b) => {
        return a < b[j] ? b[j] : a;
      }, 0)
    );
  }

  return Math.max(...maxSize) * Math.min(...maxSize);
}

solution([
  [60, 50],
  [30, 70],
  [60, 30],
  [80, 40],
]);

