function solution(board, moves) {
  let stack = [];
  let answer = 0;

  for (let i = 0; i < moves.length; i++) {
    for (let j = 0; j < board.length; j++) {
      if (board[j][moves[i] - 1] !== 0) {
        let tmp = board[j][moves[i] - 1];
        board[j][moves[i] - 1] = 0;

        if (tmp == stack[stack.length - 1]) {
          stack.pop();
          answer += 2;
        } else stack.push(tmp);
        break; // π’― λ‹¤μ forλ¬ΈμΌλ΅ λ„μ–΄κ° λ•λ” continue, ν•΄λ‹Ή forλ¬Έμ„ λλ‚Ό λ•λ” break
      }
    }
  }
  console.log(stack);

  return answer;
}

