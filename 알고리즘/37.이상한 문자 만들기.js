function solution(s) {
  s = s.split(" ").map((i) => i.split(""));

  for (let i = 0; i < s.length; i++) {
    s[i] = s[i]
      .map((j, index) => {
        return index % 2 == 0 ? j.toUpperCase() : j.toLowerCase();
      })
      .join("");
  }
  return s.join(" ");
}

